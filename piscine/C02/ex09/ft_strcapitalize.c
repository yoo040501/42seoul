char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	if (*(str + i) >= 'a' && *(str + i) <= 'z')
		*(str + i) -= 32;
	i++;
	while (*(str + i))
	{
		if (*(str + i) >= 'A' && *(str + i) <= 'Z')
			*(str + i) += 32;
		if (*(str + i) >= 'a' && *(str + i) <= 'z')
		{	
			if (!(*(str - 1 + i) >= '0' && *(str - 1 + i) <= '9')
				&& !(*(str - 1 + i) >= 'A' && *(str - 1 + i) <= 'Z')
				&& !(*(str - 1 + i) >= 'a' && *(str - 1 + i) <= 'z'))
				*(str + i) -= 32;
		}
		i++;
	}
	return (str);
}
