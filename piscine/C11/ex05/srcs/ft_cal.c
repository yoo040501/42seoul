#include "ft.h"

int	ft_cal(int a, int b, int i)
{
	if (i == 0)
		return (a + b);
	if (i == 1)
		return (a - b);
	if (i == 2)
		return (a / b);
	if (i == 3)
		return (a * b);
	if (i == 4)
		return (a % b);
	return (0);
}
